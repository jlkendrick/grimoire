package cmd

import (
	"os"
	"fmt"
	"strings"
	"time"

	"github.com/spf13/cobra"

	types "github.com/jlkendrick/grimoire/types"
	runtimes "github.com/jlkendrick/grimoire/core/runtimes"
)

func GenerateCommands(config *types.Config) ([]*cobra.Command, error) {
	commands := []*cobra.Command{}

	for _, function := range config.Functions {
		command := &cobra.Command{
			Use: function.Name,
			Run: func(cmd *cobra.Command, args []string) {
				payload := buildPayload(function, cmd)

				start := time.Now()
				runResult, err := runtimes.Run(function, payload)
				elapsed := time.Since(start)
				if err != nil {
					fmt.Fprintf(os.Stderr, "Error executing function: %v\n", err)
					os.Exit(1)
				}

				fmt.Println(string(runResult.Output))

				footerParts := []string{fmt.Sprintf("%.2fs", elapsed.Seconds())}
				if runResult.CacheStatus != "" {
					footerParts = append(footerParts, runResult.CacheStatus)
				}
				if runResult.Runtime != "" {
					footerParts = append(footerParts, runResult.Runtime)
				}
				fmt.Fprintf(os.Stderr, "\n%s %s\n", accent("◈"), dim(strings.Join(footerParts, " · ")))
			},
		}

		for _, arg := range function.Args {
			// Types might be wrong here if not generated by the init command, but we just assume the user knows what they're doing.
			// We do need to check if the default value corresponds to the type and give a helpful error message if not.
			switch arg.Type {

			case "string", "str":
				if arg.Default == nil {
					command.Flags().StringP(arg.Name, "", "", "")
					command.MarkFlagRequired(arg.Name)
					continue
				}
				if _, ok := arg.Default.(string); !ok {
					return nil, fmt.Errorf("default value for %s is not a string", arg.Name)
				}
				command.Flags().StringP(arg.Name, "", arg.Default.(string), "")

			case "integer", "int":
				if arg.Default == nil {
					command.Flags().IntP(arg.Name, "", 0, "")
					command.MarkFlagRequired(arg.Name)
					continue
				}
				if _, ok := arg.Default.(int); !ok {
					return nil, fmt.Errorf("default value for %s is not an int", arg.Name)
				}
				command.Flags().IntP(arg.Name, "", arg.Default.(int), "")

			case "boolean", "bool":
				if arg.Default == nil {
					command.Flags().BoolP(arg.Name, "", false, "")
					command.MarkFlagRequired(arg.Name)
					continue
				}
				if _, ok := arg.Default.(bool); !ok {
					return nil, fmt.Errorf("default value for %s is not a bool", arg.Name)
				}
				command.Flags().BoolP(arg.Name, "", arg.Default.(bool), "")

			case "float":
				if arg.Default == nil {
					command.Flags().Float64P(arg.Name, "", 0.0, "")
					command.MarkFlagRequired(arg.Name)
					continue
				}
				if _, ok := arg.Default.(float64); !ok {
					return nil, fmt.Errorf("default value for %s is not a float64", arg.Name)
				}
				command.Flags().Float64P(arg.Name, "", arg.Default.(float64), "")

			default:
				return nil, fmt.Errorf("unsupported type: %s", arg.Type)
			}
		}

		commands = append(commands, command)
	}

	return commands, nil
}

func buildPayload(function types.Function, cmd *cobra.Command) map[string]interface{} {
	// Initialize the dynamic payload map
	payload := make(map[string]interface{})

	// Loop through the YAML params for this specific function
	for _, param := range function.Args {
		// Must match the type strings used in GenerateCommands (including aliases like str/int/bool).
		switch param.Type {
		case "integer", "int":
			val, _ := cmd.Flags().GetInt(param.Name)
			payload[param.Name] = val
		case "string", "str":
			val, _ := cmd.Flags().GetString(param.Name)
			payload[param.Name] = val
		case "boolean", "bool":
			val, _ := cmd.Flags().GetBool(param.Name)
			payload[param.Name] = val
		case "float":
			val, _ := cmd.Flags().GetFloat64(param.Name)
			payload[param.Name] = val
		}
	}

	return payload
}